# This example, when run, will generate a PDF file called example8.pdf.
# It's not based on anything,

require 'fpdf'

pdf=FPDF.new
pdf.AddFont('Calligrapher','','calligra.rb')
pdf.AddFont('Feta','','feta16.rb')
pdf.AddFont('FetaAlphabet','','feta-alphabet16.rb')
pdf.AddPage()
pdf.SetFont('Calligrapher','',35)
pdf.Cell(0,10,'Enjoy JPEG images with FPDF!')
pdf.Ln(10)
pdf.Image('testimg.jpg',10,30,120)
pdf.SetXY(10,120)
pdf.SetFont('Calligrapher','',20)
pdf.Write(10,"This line uses an embedded TrueType font.")
pdf.Ln(10)
pdf.Write(10,"The following uses two embedded Type1 fonts.")
pdf.SetFont('Feta','',35)
pdf.SetLineWidth(0.6)
pdf.Rect(10,150,95,16)
pdf.Line(10,154,105,154)
pdf.Line(10,158,105,158)
pdf.Line(10,162,105,162)
pdf.Text(14,162,"\xae")
pdf.Text(24,158,"\x33")
pdf.SetFont('FetaAlphabet','',35)
pdf.Text(30,157,"\x36")
pdf.Text(30,165,"\x32")
pdf.SetFont('Feta','',35)
pdf.Text(40,158,"\x3e")
pdf.Text(48,156,"\x3e")
pdf.Text(56,154,"\x3d")
pdf.Text(66,156,"\x3d")
pdf.Text(76,158,"\x3d")
pdf.Text(86,162,"\x3d")
pdf.Text(96,168,"\x3d")
pdf.Line(40,159,40,168)
pdf.Line(48,157,48,166)
pdf.Line(56,155,56,164)
pdf.Line(66,157,66,166)
pdf.Line(76,159,76,168)
pdf.Line(90,161,90,151)
pdf.Line(100,167,100,157)
pdf.Output('example8.pdf')
